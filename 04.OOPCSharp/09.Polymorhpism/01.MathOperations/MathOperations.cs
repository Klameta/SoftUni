using System;
using System.Collections.Generic;
using System.Text;

namespace Operations
{
    public class MathOperations
    {
        public int Add(int first, int second) => first + second;
        public double Add(double first, double second, double third) =>  first + second + third;
        public decimal Add(decimal first, decimal second, decimal third) => first + second + third;

    }
}
